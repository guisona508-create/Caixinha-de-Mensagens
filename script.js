let timeoutId;

function mostrarMensagem(tipo) {
  const mensagem = document.getElementById("mensagem");

  const lista = mensagens[tipo];
  const texto = lista[Math.floor(Math.random() * lista.length)];

  // Para qualquer animação anterior
  clearTimeout(timeoutId);

  mensagem.innerHTML = "";
  let i = 0;

  function digitar() {
    if (i < texto.length) {
      mensagem.innerHTML += texto.charAt(i);
      i++;
      timeoutId = setTimeout(digitar, 40);
    }
  }

  digitar();
}

function criarCoracao() {
  const coracao = document.createElement("div");
  coracao.classList.add("coracao");
  coracao.innerHTML = "❤️";

  coracao.style.left = Math.random() * 100 + "vw";

  document.body.appendChild(coracao);

  setTimeout(() => {
    coracao.remove();
  }, 5000);
}

setInterval(criarCoracao, 500);

